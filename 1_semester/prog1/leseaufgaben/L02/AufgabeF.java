public class AufgabeF{
    public static void main(String[] args) {
        int a = 54;
        int b = 99;
        int c = 149;
        int d = 23423;
        
        System.out.println(a + ": " + ((a % 100) / 10));
        System.out.println(b + ": " + ((b % 100) / 10));
        System.out.println(c + ": " + ((c % 100) / 10));
        System.out.println(d + ": " + ((d % 100) / 10));   
    }
}