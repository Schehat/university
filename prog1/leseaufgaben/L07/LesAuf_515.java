public class LesAuf_515 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i + " squared = " + (i * i));
        }
        
        int i = 1;
        while (i <= 10) {
            System.out.println(i + " squared = " + (i * i));
            i++;
        }
    }
}